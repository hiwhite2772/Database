--1.Hãy cho biết mã đặt, ngày đặt của những đơn đặt hàng đã mua “Máy giặt SanYo”.
SELECT DDH.MADAT, DDH.NGAYDAT
FROM DONDATHANG DDH, CHITIETDATHANG CTDH, HANGHOA HH
WHERE DDH.MADAT = CTDH.MADAT AND CTDH.MAHH = HH.MAHH AND HH.TENHH = N'Máy giặt SanYo'

--2.Hãy cho biết tổng số lượng giao của phiếu giao hàng có mã giao là “GH01”.
SELECT SUM(SLGIAO) AS TONGSOLUONGGIAO
FROM CHITIETGIAOHANG
WHERE MAGIAO = 'GH01'

--3.Hãy cho biết mã khách hàng, tên khách hàng và số đơn đặt hàng của từng khách hàng
SELECT KH.MAKH, KH.TENKH, COUNT(DDH.MADAT) AS SODONDATHANG
FROM KHACHHANG KH, DONDATHANG DDH
WHERE KH.MAKH = DDH.MAKH
GROUP BY KH.MAKH, KH.TENKH

--4.Hãy cho biết mã giao, ngày giao của những Phiếu giao hàng có tổng số lượng giao hàng từ 20 trở lên.
SELECT PGH.MAGIAO, PGH.NGAYGIAO FROM PHIEUGIAOHANG PGH, CHITIETGIAOHANG CTGH
WHERE PGH.MAGIAO = CTGH.MAGIAO
GROUP BY PGH.MAGIAO, PGH.NGAYGIAO
HAVING SUM(CTGH.SLGIAO) >= 20

--5.Hãy cho biết mã giao, ngày giao và tổng số lượng giao của những Phiếu giao hàng có ngày giao trong tháng 2.
SELECT PGH.MAGIAO, PGH.NGAYGIAO, SUM(CTGH.SLGIAO) AS TONGSLGIAO
FROM PHIEUGIAOHANG PGH, CHITIETGIAOHANG CTGH
WHERE PGH.MAGIAO = CTGH.MAGIAO AND MONTH(PGH.NGAYGIAO) = 2
GROUP BY PGH.MAGIAO, PGH.NGAYGIAO

--6.Hãy cho biết mã hàng hóa, tên hàng hóa của những Hàng hóa có đơn giá hàng hoá từ 2000000 trở lên (danh sách được sắp xếp giảm dần theo đơn giá hàng hoá).
SELECT HH.MAHH, HH.TENHH FROM HANGHOA HH
WHERE HH.DONGIAHH >= 2000000
ORDER BY HH.DONGIAHH DESC