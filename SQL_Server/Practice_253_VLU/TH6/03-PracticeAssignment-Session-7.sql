--1. Hãy cho biết mã khách hàng, tên khách hàng đã từng đặt những đơn đặt hàng có mua “Máy giặt SanYo”.
SELECT KH.MAKH, KH.TENKH
FROM KHACHHANG KH, DONDATHANG DDH, HANGHOA HH, CHITIETDATHANG CTDH
WHERE KH.MAKH = DDH.MAKH AND CTDH.MADAT = DDH.MADAT AND HH.MAHH = CTDH.MAHH AND HH.TENHH = N'Máy giặt SanYo'

--2. Hãy cho biết số lần mặt hàng có mã “TL” được đặt hàng (số lần có mặt trong Chi tiết đặt hàng).
SELECT COUNT(*) AS SOLANMATHANG_MA_TL FROM CHITIETDATHANG WHERE MAHH = 'TL'

--3. Hãy cho biết mã giao, ngày giao và tổng số lượng giao của từng Phiếu giao hàng.
SELECT PGH.MAGIAO, PGH.NGAYGIAO, SUM(CTGH.SLGIAO) AS TONGSOLUONGGIAO
FROM PHIEUGIAOHANG PGH, CHITIETGIAOHANG CTGH
WHERE PGH.MAGIAO = CTGH.MAGIAO
GROUP BY PGH.MAGIAO, PGH.NGAYGIAO

--4. Hãy cho biết mã hàng hóa, tên hàng hóa và số lần thay đổi giá của từng Hàng hóa.
SELECT HH.MAHH, HH.TENHH, COUNT(LSG.DONGIA) AS SOLANTHAYDOI_GIA
FROM HANGHOA HH, LICHSUGIA LSG
WHERE HH.MAHH = LSG.MAHH
GROUP BY HH.MAHH, HH.TENHH

--5. Hãy cho biết mã đặt, ngày đặt, tổng số lượng đặt của những Đơn đặt hàng có ngày đặt trong tháng 2.
SELECT DDH.MADAT, DDH.NGAYDAT, SUM(CTDH.SLDAT) AS TONGSOLUONGDAT
FROM DONDATHANG DDH, CHITIETDATHANG CTDH
WHERE DDH.MADAT = CTDH.MADAT AND MONTH(DDH.NGAYDAT) = 2
GROUP BY DDH.MADAT, DDH.NGAYDAT

--6. Hãy cho biết mã hàng hóa, tên hàng hóa của những Hàng hóa có số lượng còn từ 50 đến 100 (danh sách được sắp xếp tăng dần theo số lượng).
SELECT MAHH, TENHH
FROM HANGHOA
WHERE SLCON BETWEEN 50 AND 100
ORDER BY SLCON