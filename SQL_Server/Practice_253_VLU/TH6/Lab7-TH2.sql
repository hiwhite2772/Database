--1. Hãy cho biết mã sinh viên, tên sinh viên và tên môn học mà sinh viên đó học với kết quả cuối khóa trên 7 điểm.
SELECT DISTINCT S.MASV, S.TEN, M.TENMH FROM SVIEN S, MHOC M, KQUA K, HPHAN H
WHERE S.MASV = K.MASV AND H.MAHP = K.MAHP AND H.MAMH = M.MAMH AND K.DIEM >= 7

--2. Hãy cho biết số lượng sinh viên của khoa “Công nghệ Thông tin”.
SELECT COUNT (*) SLSV FROM SVIEN S, KHOA K
WHERE K.MAKHOA = S.MAKH AND TENKHOA = N'Công nghệ Thông tin'

--3. Hãy cho biết mã khoa và số lượng môn học của từng khoa.
SELECT K.MAKHOA, COUNT(M.MAMH) AS SOLUONGMONHOC 
FROM KHOA K, MHOC M WHERE M.MAKH = K.MAKHOA 
GROUP BY K.MAKHOA

--4. Hãy cho biết mã sinh viên, tên sinh viên và điểm trung bình tích lũy của từng sinh viên.
SELECT S.MASV, S.TEN, AVG(DIEM) AS DIEMTRUNGBINHTICHLUY FROM SVIEN S, KQUA K
WHERE S.MASV = K.MASV GROUP BY S.TEN, S.MASV

--5. Hãy cho biết tên sinh viên có điểm trung bình tích lũy từ 8.0 trở lên.
SELECT S.TEN FROM SVIEN S, KQUA K
WHERE S.MASV = K.MASV  GROUP BY S.TEN, S.MASV HAVING AVG(K.DIEM) >= 8.0

--6. Hãy cho biết mã môn học, tên môn học, tên khoa (danh sách được sắp xếp tăng dần theo tên khoa).
