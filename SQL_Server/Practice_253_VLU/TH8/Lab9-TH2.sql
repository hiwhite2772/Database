CREATE TABLE CUSTOMER_T
(
	CUSTOMER_ID INT NOT NULL,
	CUSTOMER_NAME NVARCHAR(25) NOT NULL,
	CUSTOMER_GENDER BIT NULL,
	CUSTOMER_ADDRESS NVARCHAR(50),
	CITY NVARCHAR(30),
	STATE NVARCHAR(30),
	POST_CODE NVARCHAR(9),
	CONSTRAINT CUSTOMER_PK PRIMARY KEY (CUSTOMER_ID)
)


CREATE TABLE ORDER_T
(
	ORDER_ID INT NOT NULL,
	ORDER_DATE DATETIME DEFAULT (GETDATE()),
	CUSTOMER_ID INT,
	CONSTRAINT ORDER_PK PRIMARY KEY (ORDER_ID)
)

CREATE TABLE PRODUCT_T
(
	PRODUCT_ID INT NOT NULL,
	PRODUCT_DESCRIPTION NVARCHAR(50),
	PRODUCT_FINISH NVARCHAR(20) 
	CHECK (PRODUCT_FINISH IN ('Cherry','Natural Ash','White Ash', 'Red Oak','Natural Oak','Walnut')),
	STANDARD_PRICE FLOAT,
	PRODUCT_ON_HAND INT,
	CONSTRAINT PRODUCT_PK PRIMARY KEY (PRODUCT_ID)
)

CREATE TABLE ORDER_LINE_T
(
	ORDER_ID INT NOT NULL,
	PRODUCT_ID INT NOT NULL,
	ORDERED_QUATITY INT,
	CONSTRAINT ORDER_LINE_PK PRIMARY KEY (ORDER_ID,PRODUCT_ID)
)

