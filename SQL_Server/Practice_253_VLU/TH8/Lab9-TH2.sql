--Ví dụ 1: Hãy cho biết thông tin Khách hàng (CUSTOMER_ID, CUSTOMER_NAME) của những Khách hàng đã từng mua hàng hóa của Công ty (tức là đã có đơn đặt hàng)?
SELECT CUSTOMER_ID, CUSTOMER_NAME
FROM CUSTOMER_T C
WHERE EXISTS (SELECT CUSTOMER_ID 
			FROM ORDER_T O 
			WHERE C.CUSTOMER_ID = O.CUSTOMER_ID)
--Cách khác:
SELECT CUSTOMER_ID, CUSTOMER_NAME
FROM CUSTOMER_T C
WHERE CUSTOMER_ID IN (SELECT CUSTOMER_ID FROM ORDER_T)

--Ví dụ 2: Hãy cho biết những hàng hóa có giá cao nhất?
SELECT * FROM PRODUCT_T
WHERE STANDARD_PRICE = (SELECT MAX(STANDARD_PRICE) FROM PRODUCT_T)
