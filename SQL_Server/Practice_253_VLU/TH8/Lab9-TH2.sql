--Ví dụ 3: Hãy cho biết những hàng hóa đã từng được bán?
SELECT * FROM PRODUCT_T
WHERE PRODUCT_ID IN (SELECT PRODUCT_ID FROM ORDER_LINE_T)

--Ví dụ 4: Hãy cho biết những hàng hóa có giá cao nhất (cách khác)?
SELECT * FROM PRODUCT_T
WHERE STANDARD_PRICE >=ALL (SELECT STANDARD_PRICE FROM PRODUCT_T)

--Ví dụ 5: Hãy cho biết những hàng hóa đã được lập hóa đơn?
SELECT * FROM PRODUCT_T P, (SELECT PRODUCT_ID, ORDERED_QUATITY FROM ORDER_LINE_T) L
WHERE P.PRODUCT_ID = L.PRODUCT_ID

--Ví dụ 6: Hãy cho biết những hóa đơn có tổng số lượng hàng hóa là nhiều nhất?
SELECT O.ORDER_ID, O.ORDER_DATE FROM ORDER_T O, ORDER_LINE_T L
WHERE O.ORDER_ID = L.ORDER_ID
GROUP BY O.ORDER_ID, ORDER_DATE
HAVING SUM(L.ORDERED_QUATITY)>= ALL(SELECT SUM(ORDERED_QUATITY) FROM ORDER_LINE_T GROUP BY ORDER_ID)

--Ví dụ 7: Hãy cho biết những hàng hóa có giá lớn nhất?
SELECT * FROM PRODUCT_T
WHERE STANDARD_PRICE = (SELECT MAX(STANDARD_PRICE) FROM PRODUCT_T)

--Ví dụ 8: Hãy cho biết những hàng hóa nằm trong hóa đơn có mã hóa đơn là 114?
SELECT P.PRODUCT_ID, P.PRODUCT_DESCRIPTION
FROM PRODUCT_T P
WHERE EXISTS (SELECT * FROM ORDER_LINE_T L WHERE P.PRODUCT_ID = L.PRODUCT_ID AND L.ORDER_ID = 114)
